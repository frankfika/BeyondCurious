# 注册中文字体 - 支持TTC文件（需要指定subfontIndex）
def register_chinese_font():
    # 尝试各种字体配置
    font_configs = [
        # (font_name, file_path, subfontIndex)
        ('STHeiti', '/System/Library/Fonts/STHeiti Light.ttc', 0),
        ('STHeitiMedium', '/System/Library/Fonts/STHeiti Medium.ttc', 0),
        ('Songti', '/System/Library/Fonts/Supplemental/Songti.ttc', 0),
        ('PingFang', '/System/Library/Fonts/PingFang.ttc', 0),
    ]

    for font_name, fp, subfont_index in font_configs:
        if os.path.exists(fp):
            try:
                pdfmetrics.registerFont(TTFont(font_name, fp, subfontIndex=subfont_index))
                return font_name
            except Exception as e:
                continue

    # 尝试备用字体路径（用户字体目录）
    user_font_paths = [
        os.path.expanduser('~/Library/Fonts/SimHei.ttf'),
        os.path.expanduser('~/Library/Fonts/simhei.ttf'),
        '/Library/Fonts/SimHei.ttf',
    ]
    for fp in user_font_paths:
        if os.path.exists(fp):
            try:
                pdfmetrics.registerFont(TTFont('SimHei', fp))
                return 'SimHei'
            except:
                pass

    return None

FONT = register_chinese_font()

# 使用注册的字体
def get_font_name():
    return FONT if FONT else 'Helvetica'

def _font(size=12):
    return (get_font_name(), size)

def make_pdf(path, title, lines):
    """生成简单的PDF文件"""
    c = canvas.Canvas(path, pagesize=A4)
    w, h = A4
    # 标题
    fn, fs = _font(16)
    c.setFont(fn, fs)
    c.drawString(2*cm, h - 3*cm, title)
    c.setStrokeColor(colors.grey)
    c.line(2*cm, h - 3.3*cm, w - 2*cm, h - 3.3*cm)
    # 内容
    fn, fs = _font(11)
    c.setFont(fn, fs)
    y = h - 4.2*cm
    page = 1
    for line in lines:
        if y < 3*cm:
            c.setFont(*_font(9))
            c.drawString(2*cm, 1.5*cm, f"- {page} -")
            c.showPage()
            page += 1
            c.setFont(fn, fs)
            y = h - 3*cm
        c.drawString(2*cm, y, line)
        y -= 0.55*cm
    c.setFont(*_font(9))
    c.drawString(2*cm, 1.5*cm, f"- {page} -")
    c.save()

